package com.example.kioscoapp.Utils;

public class Utils {

        public static String getMonthOfTheYear(int i){
            String[] months = {"Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio", "Julio", "Agosto", "Setiembre", "Octubre"
                    , "Noviembre", "Diciembre"};

            return months[i];
        }
  
}
